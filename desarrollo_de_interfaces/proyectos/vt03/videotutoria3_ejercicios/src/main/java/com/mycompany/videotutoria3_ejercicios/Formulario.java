    private void botonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCalcularActionPerformed
        // Establecemos los valores en variables
        String nombre = textFieldNombre.getText();
        String apellidos = textFieldApellidos.getText();
        double altura = Double.parseDouble(textFieldAltura.getText());
        double peso = Double.parseDouble(textFieldPeso.getText());

        // Para establecer el genero comprobamos que radio button esta seleccionado
        String genero = "";
        if (radioButtonOtros.isSelected())
            genero = "Otros";
        else if (radioButtonMasculino.isSelected())
            genero = "Maculino";
        else if (radioButtonFemenino.isSelected())
            genero = "Femenino";
        
        // Calculamos IMC y establecemos todos los valores en las cajas de texto para mostrar la informacion
        double imc = peso / (altura * altura);
        textFieldImc.setText(Double.toString(imc));
        textAreaResultado.setText("Hola " + nombre + " " + apellidos + " tu IMC es de: " + imc + " y tu g√©nero es: " + genero);
    }//GEN-LAST:event_botonCalcularActionPerformed

