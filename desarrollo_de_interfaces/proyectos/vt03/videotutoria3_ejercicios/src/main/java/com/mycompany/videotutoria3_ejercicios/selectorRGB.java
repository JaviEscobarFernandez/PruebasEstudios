    private void botonPintarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPintarActionPerformed
        // Obtenemos los valores seleccionados en los comboBox
        int colorRojo = Integer.parseInt(comboBoxRojo.getSelectedItem().toString());
        int colorVerde = Integer.parseInt(comboBoxVerde.getSelectedItem().toString());
        int colorAzul = Integer.parseInt(comboBoxAzul.getSelectedItem().toString());
        
        // Creamos una instancia de color en base a los valores de los comboBox RGB
        Color colorBoton = new Color(colorRojo, colorVerde, colorAzul);
        
        // Establecemos el color de fondo del boton con el color obtenido
        botonPintar.setBackground(colorBoton);
    }//GEN-LAST:event_botonPintarActionPerformed

